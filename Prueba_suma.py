# Definimos los números
numero1 = 6
numero2 = 7

# Realizamos la suma
resultado = numero1 + numero2

# Mostramos el resultado en pantalla
print(f"Prueba de ejecución: {numero1} + {numero2} = {resultado}")

# Verificación lógica
if resultado == 13:
    print("✅ La prueba ha sido exitosa. ¡Todo listo para subir!")
else:
    print("❌ Algo salió mal con la suma.")